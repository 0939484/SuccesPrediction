'use strict';
class Database {

    constructor() {
        this.connection = require('./dbConnection').connection;
    }

    query(sql, args) {
        return new Promise((resolve, reject) => {
            this.connection.query(sql, args, (err, rows) => {
                if (err)
                    return reject(err);
                // console.log("SQL: " + sql + " with args: " + args + " completed");
                resolve(rows);
            });
        });
    }

    close() {
        return new Promise((resolve, reject) => {
            this.connection.end(err => {
                if (err)
                    return reject(err);
                resolve();
            });
        });
    }
}

exports.Database = Database;